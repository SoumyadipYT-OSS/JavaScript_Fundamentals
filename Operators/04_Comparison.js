// Comparison operators

let x = 5;
let y = 3;
console.log(x == y); // Output: false
console.log(x != y); // Output: true
console.log(x > y); // Output: true
console.log(x < y); // Output: false
console.log(x >= y);
console.log(x <= y);
console.log(x === y);   // strict equality operator
console.log(x !== y);   // strict inequality operator