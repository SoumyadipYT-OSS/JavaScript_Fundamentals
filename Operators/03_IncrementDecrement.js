/* Increment and Decrement operator in JavaScript */
// pre-increment
let a = 10;
console.log(++a); // Output: 11
console.log(a); // Output: 11

// post-increment
let b = 20;
console.log(b++); // Output: 20
console.log(b); // Output: 21

// pre-decrement
let c = 30;
console.log(--c); // Output: 29
console.log(c); // Output: 29

// post-decrement
let d = 40;
console.log(d--); // Output: 40
console.log(d); // Output: 39