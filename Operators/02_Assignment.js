// Assignment operators

let x = 5;
x += 3;
console.log(x); // Output: 8

let y = 5;
y -= 3;
console.log(y); // Output: 2

let z = 5;
z *= 3;
console.log(z); // Output: 15

let a = 5;
a /= 3;
console.log(a); // Output: 1.6666666666666667

let b = 5;
b %= 3;
console.log(b); // Output: 2

let c = 5;
c **= 3;
console.log(c); // Output: 125